            <li><p>8. Drs. M. Soleh (2004-2010)</p></li>
            <li><p>9. Drs. Budi Sasangka, MM (2010-2012)</p></li>
            <li><p>10. Drs. Supardi, MM (2012-2013)</p></li>
            <li><p>11. Drs. M. Sami, M.Pd (Plt. Agustus 2013 – Februari 2014)</p></li>
            <li><p>12. Drs. Dionisius Pramu Aji (2014-sekarang)</p></li>
        </ul><br/>

        <p class="justify">Sebagai sekolah berstandar nasional dalam perjalanannya sekolah ini terus berbenah diri baik dalam hal fisik berupa pembangunan dan penambahan ruang kelas baru, ruang praktek, Laboratorium serta sarana dan prasarana lainnya termasuk melengkapi peralatan praktek siswa. Hal ini terus diupayakan termasuk penerapan SMM ISO 9001:2008 pada tahun 2010 dan bersertifikat dari LSSM IQS Nomor: VED22150411. Upaya pengembangan lainya dilakukan penambahan rombongan belajar dari 15 kelas menjadi 31 kelas dan Re Enginering Kompetensi Keahlian Teknik Komputer dan Jaringan pada  tahun pelajaran 2013/2014 dan Kompetensi Multimedia pada tahun pelajaran 2015/2016.</p>
