        <h4><b>SEJARAH SINGKAT SMK NEGERI 1 JOGONALAN</b></h4>
        <p class="justify">SMK Negeri 1 Jogonalan  semula bernama SMEA Negeri Gondang Winangun adalah salah satu Sekolah Menengah Kejuruan di kabupaten Klaten yang berdiri sejak 1968 dengan Surat Keputusan Menteri Pendidikan dan Kebudayaan Republik Indonesia Nomor 28/UKK3/1968 tanggal 6 Februari 1968. Sekolah ini sangat strategis karena letaknya berada di Jalan Raya Yogya-Solo tepatnya di Dukuh Tegalmas, Desa Prawatan, Kecamatan Jogonalan, Klaten. <br/></p>
        <p class="justify">>Sejak berdiri SMK Negeri 1 Jogonalan telah mengalami pergantian Kepala Sekolah sebanyak 10 kali yakni:</p>
